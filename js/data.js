/** Получить данные города по типу */
function getCityData(city, type) {
    return CLIMATE_DATA[city] ? CLIMATE_DATA[city][type] : null;
}

/** Среднегодовое значение */
function yearlyAvg(arr) {
    return (arr.reduce((s, v) => s + v, 0) / arr.length).toFixed(1);
}

/** Годовой минимум/максимум (из помесячных средних) */
function yearlyMin(arr) {
    return Math.min(...arr);
}

function yearlyMax(arr) {
    return Math.max(...arr);
}
